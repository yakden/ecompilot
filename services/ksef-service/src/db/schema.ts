// ─────────────────────────────────────────────────────────────────────────────

export const invoicesRelations = relations(invoices, () => ({}));
export const ksefSessionsRelations = relations(ksefSessions, () => ({}));
export const ksefCredentialsRelations = relations(ksefCredentials, () => ({}));
