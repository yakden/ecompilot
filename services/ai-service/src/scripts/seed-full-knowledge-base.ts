async function seedKnowledgeBase(): Promise<void> {
  process.stdout.write(`Starting knowledge base seed — ${documents.length} documents\n`);

  let inserted = 0;
  let skipped = 0;

  for (const doc of documents) {
    try {
      await db
        .insert(knowledgeDocuments)
        .values({
          title: doc.title,
          content: doc.content,
          category: doc.category,
          language: doc.language,
          vectorId: doc.vectorId,
          indexedAt: null,
        })
        .onConflictDoNothing();

      inserted += 1;
      process.stdout.write(`  [${inserted}/${documents.length}] ${doc.title}\n`);
    } catch (err) {
      skipped += 1;
      const message = err instanceof Error ? err.message : String(err);
      process.stdout.write(`  SKIP (${message}): ${doc.title}\n`);
    }
  }

  process.stdout.write(
    `\nDone. Inserted: ${inserted}, Skipped: ${skipped}, Total: ${documents.length}\n`,
  );
}

// ─────────────────────────────────────────────────────────────────────────────
// Entry point
// ─────────────────────────────────────────────────────────────────────────────

seedKnowledgeBase()
  .then(() => {
    process.exit(0);
  })
  .catch((err: unknown) => {
    const message = err instanceof Error ? err.message : String(err);
    process.stderr.write(`Fatal error: ${message}\n`);
    process.exit(1);
  })
  .finally(() => {
    void pool.end();
  });
