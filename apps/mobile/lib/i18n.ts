export type TranslationKeys = typeof translations.en;
export type TranslationNamespace = keyof TranslationKeys;

// ─────────────────────────────────────────────────────────────────────────────
// Hook
// ─────────────────────────────────────────────────────────────────────────────

let _currentLanguage: Language = "en";

export function setI18nLanguage(lang: Language): void {
  _currentLanguage = lang;
}

export function t(): TranslationKeys {
  const lang = _currentLanguage;
  const trans = translations[lang];
  return trans as TranslationKeys;
}

export function useTranslations(): TranslationKeys {
  return t();
}
